#include <stdlib.h>
#include <string.h>

char	*ft_strdup(const char *s)
{
	char	*new;
	size_t	i;

	while (s[i])
		i++;
	new = (char *)malloc(sizeof(char) * i + 1);
	if (new == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
